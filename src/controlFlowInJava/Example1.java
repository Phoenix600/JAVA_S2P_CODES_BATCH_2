package controlFlowInJava;
// Arithmetic Operators
public class Example1 {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x++);
        System.out.println(x);
        System.out.println(++x);

        int a = 10;
        int b = 3;

        int result = a+b;
        System.out.println(result);

        result = a-b;
        System.out.println(result);

        result = a*b;
        System.out.println(result);

        result = a/b;
        System.out.println(result);

        result = a%b;
        System.out.println(result);
    }
}
